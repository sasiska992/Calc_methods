def sign(value):
    if value == 0:
        return 0
    if value > 1:
        return 1
    return -1


def get_discriminant(a, b, c):
    return b ** 2 - 4 * a * c


def default_solution(a, b, c):
    if a == 0:
        if b == 0:
            if c == 0:
                return "Множество решений"
            else:
                return "Нет решений"
        return -c / b

    d = get_discriminant(a, b, c)
    if d < 0:
        return "Нет действительных корней =("

    sqrt_d = math.sqrt(d)
    x1 = (-b - sqrt_d) / (2 * a)
    x2 = (-b + sqrt_d) / (2 * a)
    return x1, x2


def patrick_solution(a, b, c):
    if a == b == c == 0:
        return "Множество решений"
    if a == 0 and b == 0 and c != 0:
        return "Комплексные значения не подходят"
    if a == 0:
        return -c / b

    d_sqrt = get_discriminant(a, b, c) ** 0.5
    x1 = -(b + sign(b) * d_sqrt) / 2 * a

    if x1 == 0:
        return 0, 0

    x2 = c / (a * x1)
    return x1, x2


